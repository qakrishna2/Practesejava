package polymorphism.in;

public class Polymorphism {
	private int stuName;
	private int stuNo;
	public int getStuName() {
		return stuName;
	}
	public void setStuName(int stuName) {
		this.stuName = stuName;
	}
	public int getStuNo() {
		return stuNo;
	}
	public void setStuNo(int stuNo) {
		this.stuNo = stuNo;
	}
	
	

}
